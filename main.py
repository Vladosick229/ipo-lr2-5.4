numb = int(input("Введите восьмиричное число: "))

n1 = numb // 10000  
n2 = (numb // 1000) % 10 
n3 = (numb // 100) % 10   
n4 = (numb // 10) % 10   
n5 = numb % 10     

res = (n1 * 8 ** 4) + (n2 * 8 ** 3) + (n3 * 8 ** 2) + (n4 * 8 ** 1) + (n5 * 8 ** 0)

print(f"Число в десятичной системе счисления: {res}")